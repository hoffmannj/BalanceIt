package com.hofi.game.balance;

public class PlayerInfo {
	private String Name;
	private String Score;

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getScore() {
		return Score;
	}

	public void setScore(String score) {
		Score = score;
	}

}
